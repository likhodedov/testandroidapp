public class pairvalues {
    private final int Xcoord;
    private final int Ycoord;

    public pairvalues(int x, int y) {
        this.Xcoord = x;
        this.Ycoord = y;
    }

    public int getXcoord() {
        return Xcoord;
    }

    public int getYcoord() {
        return Ycoord;
    }

}
